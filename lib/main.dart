openDB() async {
  // Get a location using getDatabasesPath
  var databasesPath = await getDatabasesPath();
  print("databasesPath:${databasesPath}");

  String path = join(databasesPath, 'place.db');
  print("databasesPath With DB name:${path}");

  // try opening (will work if it exists)
  Database db;
  try {
    db = await openDatabase(path, readOnly: true);
    print("open db ok!");
  } catch (e) {
    print("Error $e");
  }

  if (db == null) {
    // Should happen only the first time you launch your application
    print("Creating new copy from asset");

    var xDBpath = await rootBundle.load(join("assets", "place.db"));
    print("Opening existing database path:${xDBpath}");

    // Copy from asset
    ByteData data = await rootBundle.load(join("assets", "place.db"));
    List<int> bytes =
        data.buffer.asUint8List(data.offsetInBytes, data.lengthInBytes);
    await new File(path).writeAsBytes(bytes);

    // open the database
    db = await openDatabase(path, readOnly: true);
  } else {
    print("Opening existing database");
  }

  // open the database
  // Database database = await openDatabase(path, version: 1);

  loadData(db);
}

